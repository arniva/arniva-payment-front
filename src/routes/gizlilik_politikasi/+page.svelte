<h1>Gizlilik ve Güvenlik Politikası</h1>
<p>
	<strong>Arniva</strong> olarak, <a href="https://odeme.arniva.tr">https://odeme.arniva.tr</a>
	adresinde sunulan web uygulamamızda kullanıcılarımızın kişisel verilerinin gizliliğini ve güvenliğini
	korumayı taahhüt ediyoruz. Bu metin, 6698 sayılı
	<strong>Kişisel Verilerin Korunması Kanunu (KVKK)</strong> uyarınca kişisel verilerin işlenmesine ilişkin
	esasları açıklar.
</p>

<h2>1. Toplanan Veriler</h2>
<p>Web uygulamamız üzerinden aşağıdaki kişisel veriler işlenmektedir:</p>
<ul>
	<li>Vergi Kimlik Numarası (VKN) veya Türkiye Cumhuriyeti Kimlik Numarası (TC Kimlik No)</li>
	<li>Ünvan (Ad Soyad veya Şirket Adı)</li>
	<li>Adres, İl, İlçe, Posta Kodu</li>
	<li>Açıklama</li>
	<li>Kart Bilgileri: Kart Sahibi, Kart Numarası, Son Kullanma Tarihi (Ay/Yıl), CVC</li>
</ul>

<h2>2. Verilerin İşlenme Amacı</h2>
<ul>
	<li>Arniva müşterilerinden ödeme alınması</li>
	<li>Kontör satış işlemlerinin gerçekleştirilmesi</li>
	<li>Ödeme sürecinde kimlik doğrulaması ve fatura bilgilerinin oluşturulması</li>
	<li>İlgili mevzuattan kaynaklanan yükümlülüklerin yerine getirilmesi</li>
</ul>

<h2>3. Veri Paylaşımı</h2>
<ul>
	<li>
		Kullanıcı tarafından girilen VKN/TC Kimlik No, Arniva’ya ait API servisleri aracılığıyla kontrol
		edilerek müşteri bilgileri otomatik doldurulur.
	</li>
	<li>
		Ödeme sırasında kart bilgileri yalnızca ilgili banka veya ödeme hizmet sağlayıcısı ile
		paylaşılır.
	</li>
	<li>Belirtilen haller dışında kişisel veriler üçüncü kişilerle paylaşılmaz.</li>
</ul>

<h2>4. Veri Saklama Süresi</h2>
<p>
	Arniva, kullanıcıların kişisel verilerini sistemlerinde saklamaz. Kart bilgileri yalnızca ödeme
	işlemi esnasında güvenli şekilde işlenir ve Arniva sistemlerinde tutulmaz. Mevzuatın zorunlu
	kıldığı hallerde, yalnızca yasal yükümlülüklerin yerine getirilmesi amacıyla işlem kayıtları
	ilgili süreler boyunca saklanabilir.
</p>

<h2>5. Veri Güvenliği</h2>
<ul>
	<li>Tüm veri transferleri SSL/TLS ile şifrelenir.</li>
	<li>
		Sistemlerde yetkisiz erişimi önlemeye yönelik erişim kontrolleri ve güvenlik duvarı uygulanır.
	</li>
	<li>
		Ödeme işlemlerinde uluslararası güvenlik standartları (ör. PCI DSS) ile uyumlu altyapılar tercih
		edilir.
	</li>
</ul>

<h2>6. Kullanıcı Hakları</h2>
<p>KVKK kapsamında aşağıdaki haklara sahipsiniz:</p>
<ul>
	<li>Kişisel verilerinizin işlenip işlenmediğini öğrenme</li>
	<li>İşleme faaliyetine ilişkin bilgi talep etme</li>
	<li>İşleme amacını ve amaca uygun kullanılıp kullanılmadığını öğrenme</li>
	<li>Yanlış veya eksik verilerin düzeltilmesini talep etme</li>
	<li>
		İşlenmesini gerektiren sebeplerin ortadan kalkması halinde silinmesini veya yok edilmesini talep
		etme
	</li>
</ul>
<p>
	Bu haklarınızı kullanmak için <a href="mailto:info@arniva.tr">info@arniva.tr</a> adresinden bize ulaşabilirsiniz.
</p>

